const num1 = parseFloat(prompt("Ingrese el num 1"));
const num2 = parseFloat(prompt("Ingrese el num 2"));

if (num1>num2){
    alert(`N1>N2. La suma es ${num1+num2}, y la diferencia es ${num1-num2}`);
} else if(num1<num2){
    alert(`N2>N1. El producto es ${n1*n2} y la division es ${num1/num2}`)
} else{
    alert("Son iguales")
};
